def format_risk_factors_display(rf: Dict) -> str:
    """Format risk factors dict for readable display."""
    labels = {
        "perinatal_insult": "Perinatal insult",
        "febrile_seizures": "Febrile seizures",
        "cns_infection": "CNS infection",
        "head_trauma": "Head trauma",
        "stroke": "Stroke",
        "brain_tumor": "Brain tumor",
        "developmental_delay": "Developmental delay",
        "family_history": "Family history of epilepsy",
        "known_genetic_syndrome": "Known genetic syndrome",
        "prior_neurosurgery": "Prior neurosurgery",
    }
    active = [labels[k] for k in labels if rf.get(k)]
    if rf.get("other_text"):
        active.append(f"Other: {rf['other_text']}")
    return ", ".join(active) if active else "None"
