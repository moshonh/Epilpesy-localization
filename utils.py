"""
utils.py
Shared utility functions: session state helpers, data validation,
export utilities, and UI helper functions.
"""

import re
import json
import datetime
from typing import Dict, Any, Optional


def initialize_session_state(st) -> None:
    """Initialize all required Streamlit session state variables."""
    defaults = {
        "patient_data": {},
        "seizure_types": [{}],  # start with one empty seizure type
        "uploaded_docs": [],
        "generated_report": None,
        "report_error": None,
        "api_key": "",
    }
    for key, val in defaults.items():
        if key not in st.session_state:
            st.session_state[key] = val


def validate_patient_data(data: Dict) -> list:
    """
    Basic validation of patient data.
    Returns a list of warning strings (empty if no warnings).
    """
    warnings = []

    age = data.get("current_age")
    onset = data.get("onset_age")
    if age and onset:
        if onset > age:
            warnings.append("Age at onset cannot be greater than current age.")
        if onset < 0 or age < 0:
            warnings.append("Ages must be non-negative.")

    if not data.get("seizure_types"):
        warnings.append("At least one seizure type description is recommended.")

    return warnings


def sanitize_text(text: Optional[str]) -> str:
    """Strip dangerous characters from free text fields."""
    if not text:
        return ""
    # Remove control characters
    text = re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f\x7f]', '', text)
    return text.strip()


def export_report_as_markdown(report: str, patient_data: Dict) -> str:
    """
    Add header metadata to the report for export.
    """
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    age = patient_data.get("current_age", "?")
    sex = patient_data.get("sex", "?")
    header = (
        f"# Epilepsy Classification and Localization Report\n\n"
        f"**Generated:** {now}  \n"
        f"**Tool:** EpiloClassify v1.0 (Clinical Decision-Support)  \n"
        f"**Patient summary:** Age {age}, {sex}  \n\n"
        f"---\n\n"
        f"> ⚠️ **DISCLAIMER:** This report is generated by an AI-assisted clinical "
        f"decision-support tool for educational and research purposes only. "
        f"It does not constitute a medical diagnosis and must not replace the "
        f"judgment of a qualified epileptologist or neurologist.\n\n"
        f"---\n\n"
    )
    return header + report


